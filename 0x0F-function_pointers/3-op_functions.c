#include "3-calc.h"

/**
 * op_add - Addition
 * @a: p1
 * @b: p2
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - Subtract
 * @a: p1
 * @b: p2
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - Multiply
 * @a: p1
 * @b: p2
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - Division
 * @a: p1
 * @b: p2
 * Return: a / b
 */
int op_div(int a, int b)
{
	int div = a / b;
	return (div);
}
/**
 * op_mod - Modulus
 * @a: p1
 * @b: p2
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
